return {
  'AndrewRadev/switch.vim',
  config = function()
    vim.g.switch_mapping = '<leader>i'
    vim.g.switch_custom_definitions = {
      {
        ['\\(\\d\\+\\(.\\d\\+\\)\\?\\)'] = '-\\1',
        ['-\\(\\d\\+\\(.\\d\\+\\)\\?\\)'] = '\\1',
      },
      {
        ['\\(\\k\\+=\\)"\\(\\k\\+\\)"'] = '\\1{`\\2`}',
        ['\\(\\k\\+=\\){[`"\']\\(\\k\\+\\)[`"\']}'] = '\\1"\\2"',
      },
      {
        ['\\(\\k\\+=\\){\\(\\(\\k\\|\\.\\)\\+\\)}'] = '\\1{`${\\2}`}',
        ['\\(\\k\\+=\\){`${\\(\\(\\k\\|\\.\\)\\+\\)}`}'] = '\\1{\\2}',
      },
    }
  end,
}
